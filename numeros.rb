cantidad_lineas = ARGV[0].to_i


cantidad_lineas.times do |i|
	i += 1
	if  i == 1
		print "1\s"
	elsif i == 2
		print "12\s"
	elsif i == 3
		print "123\s"
	elsif  i == 4
		print "1234\s"
	elsif i == 5
		print "12345\s"
	elsif i == 6
		print "123456\s"
	elsif i == 7
		print "1234567\s"
	elsif i == 8
		print "12345678\s"
	elsif i == 9
		print "123456789\s"
	end
end

		
		
